#include "libft.h"

void	ft_putstr(char const *s)
{
	char	*ptr;
	int		i;

	if (!s)
		return ;
	i = -1;
	ptr = (char *)s;
	while (ptr[++i])
		ft_putchar(ptr[i]);
}
