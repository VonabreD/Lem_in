#include "libft.h"

void	ft_putnbr(int i)
{
	char		c;
	long int	x;

	x = (long int)i;
	if (x < 0)
	{
		ft_putchar('-');
		x *= -1;
	}
	if (x >= 0)
	{
		c = x % 10 + 48;
		if (x / 10 > 0)
			ft_putnbr(x / 10);
		ft_putchar(c);
	}
}
