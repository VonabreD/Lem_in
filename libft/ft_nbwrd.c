#include "libft.h"

size_t	ft_nbwrd(char const *s, char c)
{
	if (!*s)
		return (0);
	if ((*(s + 1) == c || *(s + 1) == '\0') && *s != c)
		return (1 + ft_nbwrd(s + 1, c));
	return (ft_nbwrd(s + 1, c));
}
