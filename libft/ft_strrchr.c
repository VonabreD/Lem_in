#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = (int)ft_strlen(s);
	while (i >= 0)
		if ((char)s[i--] == c)
			return ((char *)(&s[i + 1]));
	return (0);
}
